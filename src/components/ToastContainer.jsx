import styles from "./ToastContainer.module.css";

export default function ToastContainer({ toasts }) {
  return (
    <div className={styles.container}>
      {toasts.map((toast) => (
        <div
          key={toast.id}
          className={`${styles.toast} ${styles[toast.type]}`}
        >
          {toast.message}
        </div>
      ))}
    </div>
  );
}
